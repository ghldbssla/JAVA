package day01;

public class PrintTest3 {
	public static void main(String[] args) {
		//\t : Tab역할로 출력을 이쁘게 할 때 사용
		System.out.println("키\t: "+"180");
		System.out.println("몸무게\t: "+"90");
		System.out.println("이름\t:"+"정회윤");
		//"안녕하세요 '정회윤'입니다"\o/
		System.out.println("\"안녕하세요 \'정회윤\'입니다\"\\o/");
	}
}
